


public class Rect {

    public static void main(String[] args) {
        int l, b, ar, per;
        l = Integer.parseInt("20");
        b = Integer.parseInt("30");

        ar = l * b;
        per = 2 * (l + b);
        System.out.println("Area of rectange: " + ar);
        System.out.println("Perimeter of rectange: " + per);
    }
}
